package in.cfcomputing.odin.core.services.search.domain;

import java.io.Serializable;

/**
 * Created by sheraf on 12/05/17.
 */
public class GeoCoordinates implements Serializable {
    private static final long serialVersionUID = 5649572055226107431L;
    private double lat;
    private double lon;

    public GeoCoordinates() {
    }
    public GeoCoordinates(double lat, double lon) {
        this.lat = lat;
        this.lon = lon;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }
}
